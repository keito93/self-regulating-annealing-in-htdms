"""Compute quantitative metrics for generated samples.

This script evaluates samples generated by sample.py. It does not run any
sampler or load any model. Instead, it reads the .npy sample files and
metadata.json saved by sample.py.

Metrics:
    1. W1 distance in z-space between generated samples and the reference test set.
    2. Tail probabilities in raw-space after inverse z-score transformation using
       the training statistics stored in the prepared .pt file.

This script supports using different sample directories for different metrics.
This is useful when the paper used, for example, QQ-plot samples for W1 and
independent larger samples for tail-probability evaluation.

The tail metric preserves the original notebook behavior: for each sampler,
empirical tail probabilities are computed batch-by-batch, and the reported
values are the mean and standard deviation across batches.

The inverse transform is
    x_raw = z * train_raw_std + train_raw_mean,
where train_raw_mean and train_raw_std are exactly the statistics used by
data.py for normalization.

Examples:
    # Use the same sample directory for both W1 and tail metrics.
    python quantitative_evaluation.py --sample-dir samples/xxx_trainseed4

    # Reproduce the paper-style split: W1 from QQ samples, tail from metrics samples.
    python quantitative_evaluation.py \
        --w1-sample-dir samples_qq/xxx_trainseed4 \
        --tail-sample-dir samples_metrics/yyy_trainseed4 \
        --q-list 0.999 \
        --tail-mode two-sided \
        --tail-n-batches 10 \
        --tail-batch-size 1000000

For a smoke test with only 2,000 samples per sampler:
    python quantitative_evaluation.py \
        --sample-dir samples_smoke/xxx_trainseed0 \
        --tail-n-batches 10 \
        --tail-batch-size 200
"""

from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path
from typing import Any

import numpy as np
import torch
from scipy.stats import t as student_t


SAMPLER_LABELS = {
    "g_sde": "VE-SDE",
    "t_ode": "t-ODE",
    "t_sde": "t-SDE",
    "t_sde_coeff1": "Ablated t-SDE",
}

SAMPLER_ORDER = ["g_sde", "t_ode", "t_sde", "t_sde_coeff1"]


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(description="Compute quantitative metrics.")

    parser.add_argument(
        "--sample-dir",
        type=Path,
        default=None,
        help=(
            "Sample directory used for both W1 and tail metrics. "
            "Can be overridden by --w1-sample-dir and/or --tail-sample-dir."
        ),
    )
    parser.add_argument(
        "--w1-sample-dir",
        type=Path,
        default=None,
        help="Sample directory used for W1. Default: --sample-dir.",
    )
    parser.add_argument(
        "--tail-sample-dir",
        type=Path,
        default=None,
        help="Sample directory used for tail metrics. Default: --sample-dir.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        help="Directory for saving metric files. Default: <tail_sample_dir>/metrics.",
    )
    parser.add_argument(
        "--metrics",
        nargs="+",
        choices=["w1", "tail", "all"],
        default=["all"],
        help="Metrics to compute.",
    )
    parser.add_argument(
        "--max-w1-samples",
        type=int,
        default=1_000_000,
        help="Maximum number of samples used for the W1 computation.",
    )
    parser.add_argument(
        "--w1-seed",
        type=int,
        default=0,
        help="Random seed used when subsampling for W1.",
    )
    parser.add_argument(
        "--q-list",
        type=float,
        nargs="+",
        default=[0.995, 0.999],
        help="Quantile levels used to define theoretical tail thresholds.",
    )
    parser.add_argument(
        "--tail-mode",
        choices=["right", "left", "two-sided"],
        default="two-sided",
        help="Tail probability mode.",
    )
    parser.add_argument(
        "--df-theory",
        type=float,
        default=None,
        help=(
            "Degrees of freedom of the theoretical Student-t distribution. "
            "Default: nu stored in the prepared .pt file."
        ),
    )
    parser.add_argument(
        "--tail-n-batches",
        type=int,
        default=10,
        help="Number of batches used for tail-probability evaluation.",
    )
    parser.add_argument(
        "--tail-batch-size",
        type=int,
        default=1_000_000,
        help="Batch size used for tail-probability evaluation.",
    )
    parser.add_argument(
        "--tail-batch-policy",
        choices=["error", "truncate"],
        default="error",
        help=(
            "How to handle insufficient samples for tail batches. "
            "'error' requires at least tail_n_batches * tail_batch_size samples. "
            "'truncate' uses as many full batches as available."
        ),
    )
    parser.add_argument(
        "--save-json",
        action="store_true",
        help="Also save all metrics to metrics.json.",
    )

    return parser.parse_args()


def selected_metrics(metrics_arg: list[str]) -> set[str]:
    """Resolve selected metrics."""
    selected = set(metrics_arg)
    if "all" in selected:
        return {"w1", "tail"}
    return selected


def resolve_metric_sample_dirs(args: argparse.Namespace) -> tuple[Path | None, Path | None]:
    """Resolve W1 and tail sample directories from CLI arguments."""
    metrics = selected_metrics(args.metrics)

    w1_dir = args.w1_sample_dir if args.w1_sample_dir is not None else args.sample_dir
    tail_dir = args.tail_sample_dir if args.tail_sample_dir is not None else args.sample_dir

    if "w1" in metrics and w1_dir is None:
        raise ValueError("W1 metric requires --sample-dir or --w1-sample-dir.")
    if "tail" in metrics and tail_dir is None:
        raise ValueError("Tail metric requires --sample-dir or --tail-sample-dir.")

    return w1_dir, tail_dir


def load_json(path: Path) -> dict[str, Any]:
    """Load a JSON file."""
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def save_json(path: Path, obj: Any) -> None:
    """Save an object as a JSON file."""
    with open(path, "w", encoding="utf-8") as f:
        json.dump(obj, f, indent=2, ensure_ascii=False)


def resolve_path(path_str: str, sample_dir: Path) -> Path:
    """Resolve paths stored in metadata.json."""
    path = Path(path_str)
    if path.is_absolute():
        return path

    candidates = [
        (Path.cwd() / path).resolve(),
        (sample_dir / path).resolve(),
        (sample_dir.parent / path).resolve(),
    ]

    for candidate in candidates:
        if candidate.exists():
            return candidate

    return candidates[0]


def load_metadata(sample_dir: Path) -> dict[str, Any]:
    """Load metadata.json from a sample directory."""
    metadata_path = sample_dir / "metadata.json"
    if not metadata_path.exists():
        raise FileNotFoundError(f"metadata.json not found: {metadata_path}")

    metadata = load_json(metadata_path)
    space = metadata.get("space", "z")
    if space != "z":
        raise ValueError(f"This script expects z-space samples, but metadata space is '{space}'.")

    return metadata


def compare_metadata_values(
    w1_metadata: dict[str, Any],
    tail_metadata: dict[str, Any],
    keys: list[str],
) -> list[str]:
    """Return mismatch messages for selected metadata keys."""
    mismatches: list[str] = []

    for key in keys:
        w1_value = w1_metadata.get(key, None)
        tail_value = tail_metadata.get(key, None)

        if str(w1_value) != str(tail_value):
            mismatches.append(
                f"{key}: W1={w1_value!r}, tail={tail_value!r}"
            )

    return mismatches


def check_w1_tail_metadata_consistency(
    w1_metadata: dict[str, Any],
    tail_metadata: dict[str, Any],
) -> None:
    """Check that W1 and tail samples come from the same experimental setup.

    The number of generated samples, sample batch size, and sampling seed are
    allowed to differ because W1 and tail metrics may intentionally use sample
    directories with different sizes.
    """
    keys_to_match = [
        "space",
        "train_seed",
        "data_pt_path",
        "t_run_dir",
        "g_run_dir",
        "t_ckpt",
        "g_ckpt",
        "sigma_max",
        "sigma_min",
        "n_steps_ode",
        "n_steps_sde",
        "rho",
        "nu_train",
        "nu_init_ode",
        "nu_init_sde",
        "nu_coeff",
    ]

    mismatches = compare_metadata_values(w1_metadata, tail_metadata, keys_to_match)

    if len(mismatches) > 0:
        message = "\n".join(f"  - {m}" for m in mismatches)
        raise ValueError(
            "W1 and tail sample directories appear to come from different "
            "experimental settings:\n"
            f"{message}\n"
            "If this is intentional, use matching sample directories or relax "
            "the consistency check."
        )


def check_finite(name: str, values: np.ndarray) -> None:
    """Check that all values are finite."""
    values = np.asarray(values)
    finite_mask = np.isfinite(values)

    if not finite_mask.all():
        n_bad = int((~finite_mask).sum())
        raise ValueError(f"{name} contains {n_bad} non-finite values.")


def load_prepared_pack(metadata: dict[str, Any], sample_dir: Path) -> tuple[Path, dict[str, Any]]:
    """Load the prepared .pt file referenced by metadata.json."""
    data_pt_path = metadata.get("data_pt_path", "")
    if data_pt_path == "":
        raise KeyError("metadata.json must contain a non-empty 'data_pt_path'.")

    pt_path = resolve_path(data_pt_path, sample_dir)
    if not pt_path.exists():
        raise FileNotFoundError(f"Prepared .pt file not found: {pt_path}")

    pack = torch.load(pt_path, map_location="cpu")
    return pt_path, pack


def load_reference_test_z(pack: dict[str, Any]) -> np.ndarray:
    """Load the reference test set in z-space from the prepared .pt pack."""
    reference = pack["test"].numpy().astype(np.float64).reshape(-1)
    check_finite("reference test z", reference)
    return reference


def load_train_stats(pack: dict[str, Any]) -> tuple[float, float]:
    """Load train-set raw mean and std used for z-score normalization."""
    mean = float(pack["train_raw_mean"].numpy().reshape(-1)[0])
    std = float(pack["train_raw_std"].numpy().reshape(-1)[0])

    if not np.isfinite(mean):
        raise ValueError(f"train_raw_mean is non-finite: {mean}")
    if not np.isfinite(std) or std <= 0.0:
        raise ValueError(f"train_raw_std must be finite and positive, got: {std}")

    return mean, std


def load_distribution_params(pack: dict[str, Any]) -> tuple[float, float, float]:
    """Load Student-t distribution parameters from the prepared .pt pack."""
    nu = float(pack.get("nu", 3.0))
    mu = float(pack.get("mu", 0.0))
    sigma = float(pack.get("sigma", 1.0))

    if not np.isfinite(nu) or nu <= 0.0:
        raise ValueError(f"nu must be finite and positive, got: {nu}")
    if not np.isfinite(mu):
        raise ValueError(f"mu must be finite, got: {mu}")
    if not np.isfinite(sigma) or sigma <= 0.0:
        raise ValueError(f"sigma must be finite and positive, got: {sigma}")

    return nu, mu, sigma


def load_generated_samples(metadata: dict[str, Any], sample_dir: Path) -> dict[str, np.ndarray]:
    """Load generated sample arrays listed in metadata.json."""
    sample_files = metadata.get("sample_files", {})
    if not isinstance(sample_files, dict) or len(sample_files) == 0:
        raise KeyError("metadata.json must contain a non-empty 'sample_files' dictionary.")

    samples: dict[str, np.ndarray] = {}
    for name in SAMPLER_ORDER:
        if name not in sample_files:
            continue

        path = resolve_path(sample_files[name], sample_dir)
        if not path.exists():
            raise FileNotFoundError(f"Sample file not found for {name}: {path}")

        values = np.load(path).astype(np.float64).reshape(-1)
        check_finite(f"generated samples {name}", values)
        samples[name] = values

    if len(samples) == 0:
        raise ValueError("No recognized sampler files were found in metadata.json.")

    return samples


def inverse_z_1d(z: np.ndarray, mean: float, std: float) -> np.ndarray:
    """Transform z-space samples back to raw-space using train statistics."""
    raw = np.asarray(z, dtype=np.float64).reshape(-1) * float(std) + float(mean)
    check_finite("inverse-transformed raw samples", raw)
    return raw


def wasserstein1_1d(
    x: np.ndarray,
    y: np.ndarray,
    max_samples: int = 1_000_000,
    seed: int = 0,
) -> float:
    """Compute the empirical 1D Wasserstein-1 distance."""
    rng = np.random.default_rng(seed)

    x = np.asarray(x, dtype=np.float64).reshape(-1)
    y = np.asarray(y, dtype=np.float64).reshape(-1)

    check_finite("W1 x", x)
    check_finite("W1 y", y)

    n = min(int(max_samples), len(x), len(y))
    if n <= 0:
        raise ValueError("Both input arrays must contain at least one sample.")

    if len(x) > n:
        x = x[rng.choice(len(x), size=n, replace=False)]
    else:
        x = x[:n]

    if len(y) > n:
        y = y[rng.choice(len(y), size=n, replace=False)]
    else:
        y = y[:n]

    return float(np.mean(np.abs(np.sort(x) - np.sort(y))))


def theoretical_tail_thresholds(
    q_list: np.ndarray,
    df: float,
    loc: float,
    scale: float,
    mode: str,
) -> tuple[np.ndarray, np.ndarray]:
    """Return theoretical thresholds and tail probabilities."""
    if df <= 0:
        raise ValueError("df must be positive.")
    if scale <= 0:
        raise ValueError("scale must be positive.")

    q_list = np.asarray(q_list, dtype=np.float64)

    if not np.all((0.0 < q_list) & (q_list < 1.0)):
        raise ValueError("All q values must satisfy 0 < q < 1.")

    if mode == "right":
        thresholds = loc + scale * student_t.ppf(q_list, df)
        tail_probs = 1.0 - q_list
    elif mode == "left":
        thresholds = loc + scale * student_t.ppf(1.0 - q_list, df)
        tail_probs = 1.0 - q_list
    elif mode == "two-sided":
        thresholds = loc + scale * student_t.ppf((1.0 + q_list) / 2.0, df)
        tail_probs = 1.0 - q_list
    else:
        raise ValueError("mode must be 'right', 'left', or 'two-sided'.")

    check_finite("theoretical thresholds", thresholds)
    check_finite("theoretical tail probabilities", tail_probs)

    return thresholds, tail_probs


def empirical_tail_probability(x: np.ndarray, threshold: float, loc: float, mode: str) -> float:
    """Compute empirical tail probability at a given threshold."""
    x = np.asarray(x, dtype=np.float64).reshape(-1)
    check_finite("tail evaluation samples", x)

    if mode == "right":
        return float((x > threshold).mean())
    if mode == "left":
        return float((x < threshold).mean())
    if mode == "two-sided":
        radius = abs(float(threshold) - float(loc))
        return float((np.abs(x - loc) > radius).mean())

    raise ValueError("mode must be 'right', 'left', or 'two-sided'.")


def split_into_tail_batches(
    x: np.ndarray,
    n_batches: int,
    batch_size: int,
    policy: str,
) -> list[np.ndarray]:
    """Split samples into fixed-size batches for tail evaluation."""
    if n_batches <= 0:
        raise ValueError("n_batches must be positive.")
    if batch_size <= 0:
        raise ValueError("batch_size must be positive.")

    x = np.asarray(x, dtype=np.float64).reshape(-1)
    check_finite("tail samples before batching", x)

    required = n_batches * batch_size

    if len(x) < required:
        if policy == "error":
            raise ValueError(
                f"Not enough samples for tail evaluation: required {required}, got {len(x)}. "
                "Either generate more samples with sample.py or use smaller "
                "--tail-n-batches/--tail-batch-size."
            )
        if policy == "truncate":
            n_full = len(x) // batch_size
            if n_full == 0:
                raise ValueError(
                    f"Not enough samples for even one full tail batch: "
                    f"batch_size={batch_size}, got {len(x)}."
                )
            n_batches = n_full
        else:
            raise ValueError("policy must be 'error' or 'truncate'.")

    x = x[: n_batches * batch_size]
    return [x[i * batch_size : (i + 1) * batch_size] for i in range(n_batches)]


def compute_w1_rows(
    reference_z: np.ndarray,
    samples_z: dict[str, np.ndarray],
    max_samples: int,
    seed: int,
) -> list[dict[str, Any]]:
    """Compute W1 rows in z-space."""
    rows: list[dict[str, Any]] = []

    for key in SAMPLER_ORDER:
        if key not in samples_z:
            continue

        value = wasserstein1_1d(
            samples_z[key],
            reference_z,
            max_samples=max_samples,
            seed=seed,
        )

        rows.append(
            {
                "sampler_key": key,
                "sampler": SAMPLER_LABELS[key],
                "space": "z",
                "w1": value,
                "n_generated": int(len(samples_z[key])),
                "n_reference": int(len(reference_z)),
                "max_samples": int(max_samples),
            }
        )

    return rows


def compute_tail_rows(
    samples_z: dict[str, np.ndarray],
    train_mean: float,
    train_std: float,
    q_list: list[float],
    df_theory: float,
    loc_theory: float,
    scale_theory: float,
    mode: str,
    n_batches: int,
    batch_size: int,
    batch_policy: str,
) -> list[dict[str, Any]]:
    """Compute raw-space tail probability rows with batch averaging."""
    q_array = np.asarray(q_list, dtype=np.float64)
    thresholds, p_theory = theoretical_tail_thresholds(
        q_list=q_array,
        df=df_theory,
        loc=loc_theory,
        scale=scale_theory,
        mode=mode,
    )

    rows: list[dict[str, Any]] = []

    for key in SAMPLER_ORDER:
        if key not in samples_z:
            continue

        samples_raw = inverse_z_1d(samples_z[key], mean=train_mean, std=train_std)
        batches = split_into_tail_batches(
            samples_raw,
            n_batches=n_batches,
            batch_size=batch_size,
            policy=batch_policy,
        )

        prob_by_batch = np.zeros((len(batches), len(q_array)), dtype=np.float64)
        for b, batch in enumerate(batches):
            for i, threshold in enumerate(thresholds):
                prob_by_batch[b, i] = empirical_tail_probability(
                    batch,
                    threshold=float(threshold),
                    loc=loc_theory,
                    mode=mode,
                )

        p_mean = prob_by_batch.mean(axis=0)
        p_std = prob_by_batch.std(axis=0, ddof=0)

        for i, q in enumerate(q_array):
            signed_err = p_mean[i] - float(p_theory[i])
            abs_err = abs(signed_err)
            signed_rel_err = signed_err / max(float(p_theory[i]), 1e-300)
            abs_rel_err = abs_err / max(float(p_theory[i]), 1e-300)

            rows.append(
                {
                    "sampler_key": key,
                    "sampler": SAMPLER_LABELS[key],
                    "space": "raw",
                    "tail_mode": mode,
                    "q": float(q),
                    "threshold": float(thresholds[i]),
                    "p_theory": float(p_theory[i]),
                    "p_mean": float(p_mean[i]),
                    "p_std": float(p_std[i]),
                    "signed_error": float(signed_err),
                    "absolute_error": float(abs_err),
                    "signed_relative_error": float(signed_rel_err),
                    "absolute_relative_error": float(abs_rel_err),
                    "log10_p_mean": float(np.log10(max(p_mean[i], 1e-300))),
                    "n_batches": int(len(batches)),
                    "batch_size": int(batch_size),
                    "n_generated_used": int(len(batches) * batch_size),
                    "n_generated_available": int(len(samples_raw)),
                }
            )

    rows.sort(key=lambda row: (row["q"], row["sampler_key"]))
    return rows


def save_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    """Save a list of dictionaries as a CSV file."""
    if len(rows) == 0:
        raise ValueError(f"No rows to save: {path}")

    fieldnames = list(rows[0].keys())
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"Saved CSV: {path}")


def print_w1_table(rows: list[dict[str, Any]]) -> None:
    """Print W1 results."""
    if len(rows) == 0:
        return

    print()
    print("=== W1 distance in z-space ===")
    print(f"{'sampler':<20} {'W1':>12} {'n_gen':>12} {'n_ref':>12}")
    print("-" * 60)
    for row in rows:
        print(
            f"{row['sampler']:<20} "
            f"{row['w1']:>12.6g} "
            f"{row['n_generated']:>12d} "
            f"{row['n_reference']:>12d}"
        )


def print_tail_table(rows: list[dict[str, Any]]) -> None:
    """Print raw-space tail probability results."""
    if len(rows) == 0:
        return

    q_values = sorted(set(row["q"] for row in rows))

    for q in q_values:
        subset = [row for row in rows if row["q"] == q]
        ref = subset[0]
        print()
        print(
            f"=== q = {q:g} | threshold = {ref['threshold']:.6g} "
            f"| p_theory = {ref['p_theory']:.3e} "
            f"| mode = {ref['tail_mode']} "
            f"| batches = {ref['n_batches']} x {ref['batch_size']} ==="
        )
        print(
            f"{'sampler':<20} {'p_mean':>12} {'p_std':>12} "
            f"{'log10(p)':>12} {'rel_err':>12}"
        )
        print("-" * 78)
        for row in subset:
            print(
                f"{row['sampler']:<20} "
                f"{row['p_mean']:>12.4e} "
                f"{row['p_std']:>12.4e} "
                f"{row['log10_p_mean']:>12.5g} "
                f"{row['signed_relative_error']:>12.5g}"
            )


def main() -> None:
    """Run quantitative evaluation."""
    args = parse_args()
    metrics = selected_metrics(args.metrics)
    w1_sample_dir, tail_sample_dir = resolve_metric_sample_dirs(args)

    if tail_sample_dir is not None:
        default_output_base = tail_sample_dir.resolve()
    elif w1_sample_dir is not None:
        default_output_base = w1_sample_dir.resolve()
    else:
        raise ValueError("At least one sample directory is required.")

    output_dir = args.output_dir if args.output_dir is not None else default_output_base / "metrics"
    output_dir.mkdir(parents=True, exist_ok=True)

    w1_rows: list[dict[str, Any]] = []
    tail_rows: list[dict[str, Any]] = []
    metadata_summary: dict[str, Any] = {}

    w1_metadata: dict[str, Any] | None = None
    tail_metadata: dict[str, Any] | None = None

    if "w1" in metrics:
        assert w1_sample_dir is not None
        w1_sample_dir = w1_sample_dir.resolve()
        w1_metadata = load_metadata(w1_sample_dir)
        w1_pt_path, w1_pack = load_prepared_pack(w1_metadata, w1_sample_dir)
        w1_reference_z = load_reference_test_z(w1_pack)
        w1_samples_z = load_generated_samples(w1_metadata, w1_sample_dir)

        print(f"W1_SAMPLE_DIR   : {w1_sample_dir}")
        print(f"W1_DATA_PT_PATH : {w1_pt_path}")
        print(f"W1 reference_z  : shape={w1_reference_z.shape}")
        for key, values in w1_samples_z.items():
            print(f"W1 {key:14s}: shape={values.shape}")

        w1_rows = compute_w1_rows(
            reference_z=w1_reference_z,
            samples_z=w1_samples_z,
            max_samples=args.max_w1_samples,
            seed=args.w1_seed,
        )
        metadata_summary["w1_sample_dir"] = str(w1_sample_dir)
        metadata_summary["w1_data_pt_path"] = str(w1_pt_path)

    if "tail" in metrics:
        assert tail_sample_dir is not None
        tail_sample_dir = tail_sample_dir.resolve()
        tail_metadata = load_metadata(tail_sample_dir)
        tail_pt_path, tail_pack = load_prepared_pack(tail_metadata, tail_sample_dir)
        train_mean, train_std = load_train_stats(tail_pack)
        nu_data, mu_data, sigma_data = load_distribution_params(tail_pack)
        tail_samples_z = load_generated_samples(tail_metadata, tail_sample_dir)

        df_theory = float(args.df_theory) if args.df_theory is not None else float(nu_data)
        loc_theory = float(mu_data)
        scale_theory = float(sigma_data)

        print(f"TAIL_SAMPLE_DIR   : {tail_sample_dir}")
        print(f"TAIL_DATA_PT_PATH : {tail_pt_path}")
        print(f"train mean/std    : {train_mean:.8g}, {train_std:.8g}")
        print(f"theory params     : df={df_theory}, loc={loc_theory}, scale={scale_theory}")
        print(f"tail batches      : {args.tail_n_batches} x {args.tail_batch_size}")
        for key, values in tail_samples_z.items():
            print(f"TAIL {key:12s}: shape={values.shape}")

        tail_rows = compute_tail_rows(
            samples_z=tail_samples_z,
            train_mean=train_mean,
            train_std=train_std,
            q_list=args.q_list,
            df_theory=df_theory,
            loc_theory=loc_theory,
            scale_theory=scale_theory,
            mode=args.tail_mode,
            n_batches=args.tail_n_batches,
            batch_size=args.tail_batch_size,
            batch_policy=args.tail_batch_policy,
        )
        metadata_summary["tail_sample_dir"] = str(tail_sample_dir)
        metadata_summary["tail_data_pt_path"] = str(tail_pt_path)
        metadata_summary["train_raw_mean"] = train_mean
        metadata_summary["train_raw_std"] = train_std
        metadata_summary["theory"] = {
            "df": df_theory,
            "loc": loc_theory,
            "scale": scale_theory,
            "tail_mode": args.tail_mode,
            "q_list": list(args.q_list),
        }
        metadata_summary["tail_batching"] = {
            "n_batches": int(args.tail_n_batches),
            "batch_size": int(args.tail_batch_size),
            "policy": args.tail_batch_policy,
        }

    if w1_metadata is not None and tail_metadata is not None:
        check_w1_tail_metadata_consistency(w1_metadata, tail_metadata)
        print("W1/tail metadata consistency check: passed")

    print_w1_table(w1_rows)
    print_tail_table(tail_rows)

    if len(w1_rows) > 0:
        save_csv(output_dir / "w1_metrics.csv", w1_rows)
    if len(tail_rows) > 0:
        save_csv(output_dir / "tail_metrics.csv", tail_rows)

    if args.save_json:
        save_json(
            output_dir / "metrics.json",
            {
                **metadata_summary,
                "metrics_requested": sorted(metrics),
                "w1": w1_rows,
                "tail": tail_rows,
            },
        )

    print(f"OUTPUT_DIR: {output_dir.resolve()}")
    print("Metric evaluation complete.")


